package com.mackerelpike.kapua.proxy;

public class UserInfo 
{

	private String type;
	
	private String id;
	
	private String scopeId;
	
	private String createOn;
	
	private String createdBy;
	
	private String modifiedOn;
	
	private String modifiedBy;
	
	private String optlock;
	
	private String tokenId;
	
	private String userId;
	
	private String expiresOn;
	
	private String refreshToken;
	
	private String refreshExpiresOn;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getScopeId() {
		return scopeId;
	}

	public void setScopeId(String scopeId) {
		this.scopeId = scopeId;
	}

	public String getCreateOn() {
		return createOn;
	}

	public void setCreateOn(String createOn) {
		this.createOn = createOn;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public String getModifiedOn() {
		return modifiedOn;
	}

	public void setModifiedOn(String modifiedOn) {
		this.modifiedOn = modifiedOn;
	}

	public String getModifiedBy() {
		return modifiedBy;
	}

	public void setModifiedBy(String modifiedBy) {
		this.modifiedBy = modifiedBy;
	}

	public String getOptlock() {
		return optlock;
	}

	public void setOptlock(String optlock) {
		this.optlock = optlock;
	}

	public String getTokenId() {
		return tokenId;
	}

	public void setTokenId(String tokenId) {
		this.tokenId = tokenId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getExpiresOn() {
		return expiresOn;
	}

	public void setExpiresOn(String expiresOn) {
		this.expiresOn = expiresOn;
	}

	public String getRefreshToken() {
		return refreshToken;
	}

	public void setRefreshToken(String refreshToken) {
		this.refreshToken = refreshToken;
	}

	public String getRefreshExpiresOn() {
		return refreshExpiresOn;
	}

	public void setRefreshExpiresOn(String refreshExpiresOn) {
		this.refreshExpiresOn = refreshExpiresOn;
	}
	
	
	
}
